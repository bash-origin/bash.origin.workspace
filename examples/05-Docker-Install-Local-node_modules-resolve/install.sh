#!/usr/bin/env bash

echo -e "\n########## RUN INSTALL ON DOCKER IMAGE ##########\n"

echo "VERBOSE: ${VERBOSE}"
echo "BO_VERBOSE: ${BO_VERBOSE}"
echo "UID: ${UID}"
echo "EUID: ${EUID}"
echo "GROUPS: ${GROUPS}"

echo "Install workspace package ..."

git clone https://github.com/bash-origin/bash.origin.workspace.git

pushd "bash.origin.workspace"
    # '--unsafe-perm' is needed to not use the 'nobody' group.
    npm install --unsafe-perm
popd
